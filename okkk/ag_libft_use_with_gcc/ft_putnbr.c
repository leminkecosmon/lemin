#include "libft.h"

void	ft_putnbr(int n)
{
	int c;

	c = 0;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
	}
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	if (n >= 0)
	{
		if (n > 9)
		{
			ft_putnbr(n / 10);
		}
		c = n % 10;
		ft_putchar(c + '0');
	}
}
